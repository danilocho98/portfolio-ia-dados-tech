# (c) Jacobi
def _header_iterativo(n, rotulo_var="I"):
    cols = ["Iter"] + [f"{rotulo_var}{k+1}" for k in range(n)] + ["erro"]
    header = "| " + " | ".join([f"{c:^14}" for c in cols]) + " |"
    sep    = "|-" + "-|-".join(["-"*14 for _ in cols]) + "-|"
    return header, sep, cols

def jacobi(A, b, x0=None, eps=1e-3, maxit=10_000, imprimir_tabela=True, rotulo_var="I"):
    A = A.astype(float); b = b.astype(float)
    n = len(b)
    x = np.zeros(n) if x0 is None else np.array(x0, dtype=float).copy()
    D = np.diag(A)
    R = A - np.diagflat(D)

    tabela = []
    if imprimir_tabela:
        print("#\033[1m \033[4m EXERCÍCIO (c) — Jacobi (sistema possivelmente permutado) \033[0m")
        header, sep, _ = _header_iterativo(n, rotulo_var)
        print(header); print(sep)

    for k in range(maxit):
        x_new = (b - R @ x) / D
        err = np.max(np.abs(x_new - x))
        tabela.append((k, *x_new.tolist(), err))
        if imprimir_tabela:
            vals = [f"{v: .10f}" for v in x_new]
            linha = f"{k:4d} | " + " | ".join(vals + [f"{err: .6e}"]) + " |"
            print(linha)
        x = x_new
        if err < eps:
            break
    return x, tabela

