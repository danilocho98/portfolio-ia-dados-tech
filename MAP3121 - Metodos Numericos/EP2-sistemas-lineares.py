# (b) Eliminação de Gauss com Pivotamento Parcial (geral n x n)

def gauss_pp(A_in, b_in, verbose=True):
    """
    Eliminação de Gauss com pivotamento parcial.
    Retorna (x, logs), onde 'logs' contém rótulos + estados de [A|b].
    """
    A = A_in.astype(float).copy()
    b = b_in.astype(float).copy()
    n = len(b)
    logs = []

    for k in range(n-1):
        # Pivotamento parcial
        p = k + np.argmax(np.abs(A[k:, k]))
        if np.isclose(A[p, k], 0.0):
            raise ValueError("Matriz singular ou pivô nulo.")
        if p != k:
            A[[k, p]] = A[[p, k]]
            b[[k, p]] = b[[p, k]]
            if verbose:
                logs.append(("Permuta de linhas", A.copy(), b.copy()))

        # Antes da eliminação
        if verbose:
            logs.append((f"Antes eliminação col {k}", A.copy(), b.copy()))

        # Eliminação
        for i in range(k+1, n):
            m = A[i, k] / A[k, k]
            A[i, k:] -= m * A[k, k:]
            b[i] -= m * b[k]

        # Após eliminação
        if verbose:
            logs.append((f"Após eliminação col {k}", A.copy(), b.copy()))

    # Retrosubstituição
    x = np.zeros(n)
    for i in range(n-1, -1, -1):
        x[i] = (b[i] - A[i, i+1:] @ x[i+1:]) / A[i, i]

    if verbose:
        print("Matrizes intermediárias [A|b] até triangular superior:")
        for tag, Alog, blog in logs:
            print_aug(Alog, blog, tag)

    return x, logs

