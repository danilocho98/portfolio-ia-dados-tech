# (e) Gauss–Seidel

def gauss_seidel(A, b, x0=None, eps=1e-3, maxit=10_000, imprimir_tabela=True, rotulo_var="I"):
    A = A.astype(float); b = b.astype(float)
    n = len(b)
    x = np.zeros(n) if x0 is None else np.array(x0, dtype=float).copy()

    tabela = []
    if imprimir_tabela:
        print("# (e) Gauss–Seidel")
        header, sep, _ = _header_iterativo(n, rotulo_var)
        print(header); print(sep)

    for k in range(maxit):
        x_old = x.copy()
        for i in range(n):
            s1 = A[i, :i] @ x[:i]
            s2 = A[i, i+1:] @ x_old[i+1:]
            x[i] = (b[i] - s1 - s2) / A[i, i]
        err = np.max(np.abs(x - x_old))
        tabela.append((k, *x.tolist(), err))
        if imprimir_tabela:
            vals = [f"{v: .10f}" for v in x]
            linha = f"{k:4d} | " + " | ".join(vals + [f"{err: .6e}"]) + " |"
            print(linha)
        if err < eps:
            break
    return x, tabela

