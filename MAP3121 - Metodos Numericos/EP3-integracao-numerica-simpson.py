# Calcular R = T / T_Galileu em função de θ₀
# utilizando a regra dos trapézios, evitando a singularidade.

M_TRAP = 4000  # número de subintervalos

def R_pendulo(theta0, M=M_TRAP):
    """
    R(theta0) = (2/pi) * ∫_0^{pi/2} 1/sqrt(1 - k^2 sin^2 xi) dxi
    Método dos trapézios em [0, pi/2 - gap] para evitar o ponto singular.
    """
    k2 = (1.0 - math.cos(theta0)) / 2.0
    a = 0.0
    b = math.pi / 2.0

    # corta um pequeno trecho antes do ponto singular (xi = pi/2)
    gap = 1e-5
    b_eff = b - gap

    h = (b_eff - a) / M

    def integrando(xi):
        s = math.sin(xi)
        den2 = 1.0 - k2 * s * s
        if den2 <= 0.0:
            den2 = 1e-300
        return 1.0 / math.sqrt(den2)

    acc = 0.5 * (integrando(a) + integrando(b_eff))
    for j in range(1, M):
        xi = a + j * h
        acc += integrando(xi)

    integral = acc * h
    return (2.0 / math.pi) * integral  # R = T/T_Galileu

