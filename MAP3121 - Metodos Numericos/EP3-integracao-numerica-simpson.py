    print("---|---------------------|---------------------------|-------------------")

    p_list, err_list = [], []
    for p in range(P_MIN, P_MAX + 1):
        N = 1 << p
        Inum = simpson_composto(A, B, N, dtype=dt)
        err = abs(Inum - I_EXATO)
        print(f"\033[1m{p:2d}\033[0m | {N:21d} | {Inum: .16e} | {err: .6e}")
        if err > 0 and np.isfinite(err):
            p_list.append(p); err_list.append(err)
    return np.array(p_list, float), np.array(err_list, float)

